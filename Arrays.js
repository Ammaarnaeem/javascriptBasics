// Arrays in JS are considered as an object.

let list=['ali','usman',1,2];
console.log(list);

// we have dynamic array in js means we can add as much elements as we want and of any type.
list[4]='asad';
console.log(list);

//accessing index 0 of array.
console.log(list[0]);

// we can change elements with any other data type element.
list[1]=78;
console.log(list[1]);

// checking length of an array
console.log(list.length);